class Program72
{
    public static void main(String[]args){
        //swap two numbers
        int a=11;
        int b=55;
        System.out.println(a);
        System.out.println(b);
        int temp;
        temp=a;
        a=b;
        b=temp;
        System.out.println(a);
        System.out.println(b);
    }
}